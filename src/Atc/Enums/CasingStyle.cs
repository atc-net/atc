namespace Atc;

/// <summary>
/// Defines different text casing styles commonly used in programming and naming conventions.
/// </summary>
public enum CasingStyle
{
    /// <summary>
    /// No specific casing style specified.
    /// </summary>
    None,

    /// <summary>
    /// The camel case is formatted like this: "camelCase".
    /// </summary>
    CamelCase,

    /// <summary>
    /// The kebab case is formatted like this: "kebab-case".
    /// </summary>
    KebabCase,

    /// <summary>
    /// The pascal case is formatted like this: "PascalCase".
    /// </summary>
    PascalCase,

    /// <summary>
    /// The snake case is formatted like this: "snake_case".
    /// </summary>
    SnakeCase,
}